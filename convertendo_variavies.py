
#converter para float e int
preco = 10
print(preco)

preco = float(preco)
print(preco)

preco = int(preco)
print(preco)

#converter para "float"( / ) e "int"( // ), por divisão 
print(preco / 2)

print(preco // 2)

#numérico para string

preco = 10.50
idade = 28

print(str(preco))
print(str(idade))

texto = f"idade {idade} preco {preco}"
print(texto)